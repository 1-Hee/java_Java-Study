package js08_switch;

public class Main {

	public static void main(String[] args) {
		
		int a = 2;
		switch(a) {
			case 1 :
				System.out.println("1임다");
				break;
			case 2:
				System.out.println("2임다");
				break;
			case 3:
				System.out.println("3임다");
				break;
			case 4:
				System.out.println("4임다");
				break;
			default :
				System.out.println("모르겠심다");
				break;
			
		}

	}

}
